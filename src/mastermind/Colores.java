package mastermind;

/**
 * Clase dedicada a los colores que podemos elegir para las fichas de las casillas del juego
 * 
 * @author Ana Castilla
 * @version 1.0
 * @since 1.0
 *
 */

public final class Colores {
	
	/*
	 * Constantes de todos los colores disponibles
	 */

	public static final String RESET = "\u001B[0m";
	public static final String FONDO_BLANCO = "\u001B[107m";
	public static final String FONDO_NEGRO = "\u001B[40m";
	public static final String FONDO_AMARILLO = "\u001B[43m";
	public static final String FONDO_AZUL = "\u001B[44m";
	public static final String FONDO_MORADO = "\u001B[45m";
	public static final String FONDO_ROJO = "\u001B[41m";
	public static final String FONDO_VERDE = "\u001B[42m";
	public static final String FONDO_GRIS = "\u001B[100m";
	public static final String FONDO_CELESTE = "\033[0;106m";
	public static final String FONDO_ROSA = "\033[0;105m";
	public static final String VERDE_CLARO = "\033[0;102m";
	public static final String FONDO_ROJO_CLARO = "\033[0;101m";
	
	public static final String LETRA_VERDE = "\u001B[32m";
	public static final String LETRA_AMARILLO = "\u001B[33m";
	public static final String LETRA_ROJO = "\u001B[31m";
	
}
